  // --- language state (default: Telegram language_code -> ru fallback)
  let currentLang = "ru";

  function normalizeLang(code) {
    const c = String(code || "").toLowerCase();
    if (c.startsWith("uz")) return "uz";
    if (c.startsWith("en")) return "en";
    if (c.startsWith("ru")) return "ru";
    return "ru";
  }

  // Simple templating: t("key", {name:"..."})
  function template(str, vars) {
    if (!vars) return str;
    return String(str).replace(/\{(\w+)\}/g, (_, k) => {
      const v = vars[k];
      return v === undefined || v === null ? "" : String(v);
    });
  }

  function t(key, vars) {
    const lang = currentLang;
    const langPack = DICT[lang] || DICT.ru;
    const ruPack = DICT.ru;

    const raw =
      (langPack && langPack[key]) ||
      (ruPack && ruPack[key]);

    if (!raw) {
      try { console.warn(`Missing translation: [${lang}] ${key}`); } catch {}
      return `[${lang}] ${key}`;
    }

    return template(raw, vars);
  }

  function setLang(code) {
    currentLang = normalizeLang(code);
    document.documentElement.lang = currentLang;
  }

  function getLang() {
    return currentLang;
  }

  window.i18n = {
    t,
    setLang,
    getLang,
    normalizeLang
  };
